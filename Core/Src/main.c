      if(str[0] == 0x6F && str[1] == 0x6E && str[2] == 0x6E ) {
        onn = 1;
        off = 0;
      } else if(str[0] == 0x6F && str[1] == 0x66 && str[2] == 0x66 ) {
        onn = 0;
        off = 1;
      }


